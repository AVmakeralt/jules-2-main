// =========================================================================
// Statistics
// =========================================================================

/// Statistics from a superoptimization run.
#[derive(Debug, Clone, Default)]
pub struct SuperoptStats {
    /// Total number of expression nodes visited during the walk.
    pub expressions_visited: usize,
    /// Number of expressions that were actually replaced with optimized versions.
    pub expressions_optimized: usize,
    /// Number of candidate programs generated by MCTS search.
    pub mcts_candidates_generated: usize,
    /// Number of MCTS candidates that passed verification.
    pub mcts_candidates_verified: usize,
    /// Number of SMT (CEGIS) verifications performed.
    pub smt_verifications: usize,
    /// Number of semantic rewrite rules that fired (Stage 1).
    pub semantic_rewrites: usize,
    /// Number of candidates rejected by known-bits analysis (Stage 2).
    pub known_bits_rejections: usize,
    /// Number of candidates rejected because uarch cost was not better (Stage 5).
    pub uarch_rejections: usize,
    /// Number of candidates processed by the machine-instr pipeline (Stage 6).
    pub machine_instr_candidates: usize,
    /// Number of candidates rejected by machine-instr cost model (Stage 6).
    pub machine_instr_rejections: usize,
    /// Total wall-clock time spent in the pass, in microseconds.
    pub total_time_us: u64,
}

// =========================================================================
// Configuration
// =========================================================================

/// Configuration for the unified superoptimizer pass.
#[derive(Debug, Clone)]
pub struct SuperoptConfig {
    /// Maximum number of nodes in an expression's `Instr` tree for it to be
    /// considered for MCTS search. Larger expressions are skipped.
    pub max_expr_size: usize,
    /// Whether to verify equivalence with CEGIS before accepting a candidate.
    pub verify_equivalence: bool,
    /// Whether to use the uarch cost model to validate that the optimized
    /// version is actually cheaper on the target CPU.
    pub use_uarch_cost_model: bool,
    /// Bit width for SMT bitvector encoding (typically 64).
    pub smt_bitwidth: u32,
    /// Maximum number of CEGIS iterations per verification.
    pub cegis_max_iterations: usize,
}

impl Default for SuperoptConfig {
    fn default() -> Self {
        Self {
            max_expr_size: 64,
            verify_equivalence: true,
            use_uarch_cost_model: true,
            smt_bitwidth: 64,
            cegis_max_iterations: 10,
        }
    }
}

// =========================================================================
// SuperoptPass — the unified pipeline
// =========================================================================

/// The unified superoptimizer pass.
///
/// Wire together the disconnected superoptimizer modules into a single
/// compilation pipeline entry point.  Call `optimize_program(&mut Program)`
/// to run all enabled stages.
///
/// S10+S13 fix: Reuses a single MctsSuperoptimizer across expressions
/// and caches TargetConfig to avoid CPUID per expression.
pub struct SuperoptPass {
    config: SuperoptConfig,
    stats: SuperoptStats,
    /// S10 fix: Reuse a single MctsSuperoptimizer across expressions
    /// to avoid recreating HardwareCostModel (CPUID) per expression.
    #[cfg(any(feature = "core-superopt", feature = "gnn-optimizer"))]
    mcts_optimizer: Option<MctsSuperoptimizer>,
    /// S13 fix: Cache TargetConfig once instead of calling detect() per expression.
    #[cfg(feature = "core-superopt")]
    cached_target: Option<TargetConfig>,
}

impl SuperoptPass {
    /// Create a new superoptimizer pass with the given configuration.
    pub fn new(config: SuperoptConfig) -> Self {
        Self {
            config,
            stats: SuperoptStats::default(),
            #[cfg(any(feature = "core-superopt", feature = "gnn-optimizer"))]
            mcts_optimizer: None, // Lazy-initialized on first use
            #[cfg(feature = "core-superopt")]
            cached_target: None, // Lazy-initialized on first use
        }
    }

    /// Create a superoptimizer pass with default configuration.
    pub fn default_pass() -> Self {
        Self::new(SuperoptConfig::default())
    }

    /// Run the full superoptimization pipeline on a program, modifying it
    /// in place.  Returns a reference to the accumulated statistics.
    pub fn optimize_program(&mut self, program: &mut Program) -> &SuperoptStats {
        let start = Instant::now();

        match program.superopt_mode {
            SuperoptMode::None => {
                // Skip superoptimization entirely.
            }
            SuperoptMode::All => {
                self.run_pipeline(program, /* full_mode */ true);
            }
            SuperoptMode::MctsSuperopt => {
                // MCTS-only: no semantic rewrites, but full verification.
                self.run_pipeline(program, /* full_mode */ true);
            }
            SuperoptMode::EGraph | SuperoptMode::MlSuperopt => {
                // Semantic rewrites + MCTS fast config, skip SMT.
                self.run_pipeline(program, /* full_mode */ false);
            }
        }

        self.stats.total_time_us = start.elapsed().as_micros() as u64;
        &self.stats
    }

    /// Consume the pass and return the statistics.
    pub fn into_stats(self) -> SuperoptStats {
        self.stats
    }

    /// Return a reference to the accumulated statistics.
    pub fn stats(&self) -> &SuperoptStats {
        &self.stats
    }

    // ─────────────────────────────────────────────────────────────────────
    // Internal pipeline driver
    // ─────────────────────────────────────────────────────────────────────

    fn run_pipeline(&mut self, program: &mut Program, full_mode: bool) {
        // ── Stage 1: Semantic Rewrites (gnn-optimizer) ────────────────────
        #[cfg(feature = "gnn-optimizer")]
        {
            let mut semantic = SemanticSuperoptimizer::new();
            let patterns_before = semantic.patterns_matched;
            semantic.optimize_program(program);
            let patterns_after = semantic.patterns_matched;
            self.stats.semantic_rewrites += (patterns_after - patterns_before) as usize;
        }

        // ── Stages 2-5: Per-expression MCTS + verification + cost ────────
        self.walk_program(program, full_mode);
    }

    // ─────────────────────────────────────────────────────────────────────
    // AST walker — visits every expression in the program
    // ─────────────────────────────────────────────────────────────────────

    /// Walk the entire program and optimize each eligible expression.
    fn walk_program(&mut self, program: &mut Program, full_mode: bool) {
        for item in &mut program.items {
            self.walk_item(item, /* is_hot */ false, full_mode);
        }
    }

    fn walk_item(&mut self, item: &mut Item, is_hot: bool, full_mode: bool) {
        match item {
            Item::Fn(fn_decl) => {
                // Function bodies are considered "warm" — they may be called
                // multiple times, so optimization is worthwhile.
                if let Some(body) = &mut fn_decl.body {
                    self.walk_block(body, is_hot || true, full_mode);
                }
            }
            Item::System(sys_decl) => {
                // System bodies run every tick — always hot.
                self.walk_block(&mut sys_decl.body, true, full_mode);
            }
            Item::Const(const_decl) => {
                self.walk_expr(&mut const_decl.value, is_hot, full_mode);
            }
            _ => {
                // Structs, enums, components, etc. have no executable bodies.
            }
        }
    }

    fn walk_block(&mut self, block: &mut crate::compiler::ast::Block, is_hot: bool, full_mode: bool) {
        for stmt in &mut block.stmts {
            self.walk_stmt(stmt, is_hot, full_mode);
        }
        if let Some(tail) = &mut block.tail {
            self.walk_expr(tail, is_hot, full_mode);
        }
    }

    fn walk_stmt(&mut self, stmt: &mut Stmt, is_hot: bool, full_mode: bool) {
        match stmt {
            Stmt::Let { init: Some(expr), .. } => {
                self.walk_expr(expr, is_hot, full_mode);
            }
            Stmt::Expr { expr, .. } => {
                self.walk_expr(expr, is_hot, full_mode);
            }
            Stmt::Return { value: Some(expr), .. } => {
                self.walk_expr(expr, is_hot, full_mode);
            }
            Stmt::If { cond, then, else_, .. } => {
                self.walk_expr(cond, is_hot, full_mode);
                self.walk_block(then, is_hot, full_mode);
                if let Some(else_box) = else_ {
                    match else_box.as_mut() {
                        crate::compiler::ast::IfOrBlock::If(nested_if) => {
                            self.walk_stmt(nested_if, is_hot, full_mode);
                        }
                        crate::compiler::ast::IfOrBlock::Block(b) => {
                            self.walk_block(b, is_hot, full_mode);
                        }
                    }
                }
            }
            Stmt::While { cond, body, .. } => {
                self.walk_expr(cond, true, full_mode);
                self.walk_block(body, true, full_mode);
            }
            Stmt::Loop { body, .. } => {
                self.walk_block(body, true, full_mode);
            }
            Stmt::ForIn { iter, body, .. } => {
                self.walk_expr(iter, true, full_mode);
                self.walk_block(body, true, full_mode);
            }
            Stmt::EntityFor { body, .. } => {
                self.walk_block(body, true, full_mode);
            }
            Stmt::Match { expr, arms, .. } => {
                self.walk_expr(expr, is_hot, full_mode);
                for arm in arms.iter_mut() {
                    if let Some(guard) = &mut arm.guard {
                        self.walk_expr(guard, is_hot, full_mode);
                    }
                    self.walk_expr(&mut arm.body, is_hot, full_mode);
                }
            }
            Stmt::Break { value: Some(expr), .. } => {
                self.walk_expr(expr, is_hot, full_mode);
            }
            Stmt::Item(item_box) => {
                self.walk_item(item_box, is_hot, full_mode);
            }
            _ => {
                // Let with no init, Break with no value, etc.
            }
        }
    }

    /// Walk and potentially optimize a single expression node, recursing
    /// into children first (bottom-up optimization).
    fn walk_expr(&mut self, expr: &mut Expr, is_hot: bool, full_mode: bool) {
        self.stats.expressions_visited += 1;

        // ── Recurse into children first (bottom-up) ──────────────────────
        self.recurse_into_children(expr, is_hot, full_mode);

        // ── Try to optimize this expression ──────────────────────────────
        self.try_optimize_expr(expr, is_hot, full_mode);
    }

    /// Recursively walk into child expressions.
    fn recurse_into_children(&mut self, expr: &mut Expr, is_hot: bool, full_mode: bool) {
        match expr {
            Expr::BinOp { lhs, rhs, .. } => {
                self.walk_expr(lhs, is_hot, full_mode);
                self.walk_expr(rhs, is_hot, full_mode);
            }
            Expr::UnOp { expr: inner, .. } => {
                self.walk_expr(inner, is_hot, full_mode);
            }
            Expr::Assign { target, value, .. } => {
                self.walk_expr(target, is_hot, full_mode);
                self.walk_expr(value, is_hot, full_mode);
            }
            Expr::Field { object, .. } => {
                self.walk_expr(object, is_hot, full_mode);
            }
            Expr::Index { object, indices, .. } => {
                self.walk_expr(object, is_hot, full_mode);
                for idx in indices.iter_mut() {
                    self.walk_expr(idx, is_hot, full_mode);
                }
            }
            Expr::Call { func, args, named, .. } => {
                self.walk_expr(func, is_hot, full_mode);
                for arg in args.iter_mut() {
                    self.walk_expr(arg, is_hot, full_mode);
                }
                for (_, val) in named.iter_mut() {
                    self.walk_expr(val, is_hot, full_mode);
                }
            }
            Expr::MethodCall { receiver, args, .. } => {
                self.walk_expr(receiver, is_hot, full_mode);
                for arg in args.iter_mut() {
                    self.walk_expr(arg, is_hot, full_mode);
                }
            }
            Expr::VecCtor { elems, .. } | Expr::ArrayLit { elems, .. } => {
                for elem in elems.iter_mut() {
                    self.walk_expr(elem, is_hot, full_mode);
                }
            }
            Expr::IfExpr { cond, then, else_, .. } => {
                self.walk_expr(cond, is_hot, full_mode);
                self.walk_block(then, is_hot, full_mode);
                if let Some(else_block) = else_ {
                    self.walk_block(else_block, is_hot, full_mode);
                }
            }
            Expr::Closure { body, .. } => {
                self.walk_expr(body, is_hot, full_mode);
            }
            Expr::Block(block) => {
                self.walk_block(block, is_hot, full_mode);
            }
            Expr::Tuple { elems, .. } => {
                for elem in elems.iter_mut() {
                    self.walk_expr(elem, is_hot, full_mode);
                }
            }
            Expr::Cast { expr: inner, .. } => {
                self.walk_expr(inner, is_hot, full_mode);
            }
            Expr::MatMul { lhs, rhs, .. }
            | Expr::HadamardMul { lhs, rhs, .. }
            | Expr::HadamardDiv { lhs, rhs, .. }
            | Expr::TensorConcat { lhs, rhs, .. }
            | Expr::KronProd { lhs, rhs, .. }
            | Expr::OuterProd { lhs, rhs, .. } => {
                self.walk_expr(lhs, is_hot, full_mode);
                self.walk_expr(rhs, is_hot, full_mode);
            }
            Expr::Grad { inner, .. } => {
                self.walk_expr(inner, is_hot, full_mode);
            }
            Expr::Pow { base, exp, .. } => {
                self.walk_expr(base, is_hot, full_mode);
                self.walk_expr(exp, is_hot, full_mode);
            }
            Expr::Range { lo, hi, .. } => {
                if let Some(lo) = lo {
                    self.walk_expr(lo, is_hot, full_mode);
                }
                if let Some(hi) = hi {
                    self.walk_expr(hi, is_hot, full_mode);
                }
            }
            Expr::StructLit { fields, .. } => {
                for (_, val) in fields.iter_mut() {
                    self.walk_expr(val, is_hot, full_mode);
                }
            }
            // Leaf nodes — no children to recurse into.
            Expr::IntLit { .. }
            | Expr::FloatLit { .. }
            | Expr::BoolLit { .. }
            | Expr::StrLit { .. }
            | Expr::Ident { .. }
            | Expr::Path { .. } => {}
        }
    }

    // ─────────────────────────────────────────────────────────────────────
    // Core per-expression optimization pipeline
    // ─────────────────────────────────────────────────────────────────────

    /// Try to optimize a single expression through stages 2-5.
    ///
    /// Returns `true` if the expression was replaced with a better one.
    fn try_optimize_expr(&mut self, expr: &mut Expr, is_hot: bool, full_mode: bool) -> bool {
        // ── Convert Expr → MctsInstr ─────────────────────────────────────
        //
        // Only pure arithmetic/logical expressions can be optimized.
        // Anything involving calls, field access, assignments, etc. is
        // skipped because the MCTS search space doesn't model side effects.

        #[cfg(any(feature = "core-superopt", feature = "gnn-optimizer"))]
        {
            let original_instr = match MctsInstr::from_expr(expr) {
                Some(instr) => instr,
                None => return false, // Can't represent in MCTS search space.
            };

            // Skip expressions that are too large (MCTS would be too slow).
            if instr_size(&original_instr) > self.config.max_expr_size {
                return false;
            }

            // Skip trivially simple expressions (single constants/variables).
            if !is_interesting_for_superopt(&original_instr) {
                return false;
            }

            // ── Stage 3: MCTS Tree Search ────────────────────────────────
            // S10 fix: Reuse MctsSuperoptimizer across expressions instead of
            // creating a new one per expression (which runs CPUID each time).
            // S11 fix: Use hotpath() config instead of maximum() even for hot
            // expressions — maximum() has 5000 sims and 1s budget per expr!
            let mcts_config = if is_hot {
                MctsConfig::hotpath() // Was maximum() — 5000 sims × 1s = too slow
            } else {
                MctsConfig::fast()
            };

            // Fix H3: Only recreate the MCTS optimizer when needed.
            // Previously, BOTH branches of the if/else created a new optimizer,
            // wasting CPUID/HardwareCostModel initialization cost every expression.
            // Now we call reset_for_new_expr() to reuse the existing optimizer
            // when only the expression changes (not the config).
            let need_new_optimizer = match &self.mcts_optimizer {
                None => true,
                Some(existing) => existing.config_hash() != mcts_config.config_hash(),
            };

            if need_new_optimizer || self.mcts_optimizer.is_none() {
                self.mcts_optimizer = Some(MctsSuperoptimizer::new(mcts_config));
            } else {
                // Reuse the existing optimizer — just reset for the new expression.
                // This avoids re-initializing CPUID, hardware cost model, etc.
                self.mcts_optimizer.as_mut().unwrap().reset_for_new_expr();
            }

            let candidate_expr = match self.mcts_optimizer.as_mut().unwrap().optimize(expr) {
                Some(e) => e,
                None => return false, // No improvement found.
            };
            self.stats.mcts_candidates_generated += 1;

            // Convert candidate to Instr for verification stages.
            let candidate_instr = match MctsInstr::from_expr(&candidate_expr) {
                Some(instr) => instr,
                None => return false, // Candidate can't be represented? Skip.
            };

            // ── Stage 2: Known-Bits Pruning ──────────────────────────────
            #[cfg(feature = "core-superopt")]
            {
                let env = FxHashMap::default(); // No variable type info available.
                if !known_bits::may_be_equivalent(&original_instr, &candidate_instr, &env) {
                    self.stats.known_bits_rejections += 1;
                    return false;
                }
            }

            // ── Stage 4: SMT Verification ────────────────────────────────
            #[cfg(feature = "core-superopt")]
            {
                if self.config.verify_equivalence && full_mode {
                    let num_vars = original_instr
                        .variables()
                        .len()
                        .max(candidate_instr.variables().len());
                    let mut verifier =
                        CegisVerifier::new(self.config.smt_bitwidth, num_vars, self.config.cegis_max_iterations);
                    let result = verifier.verify(&original_instr, &candidate_instr);
                    self.stats.smt_verifications += 1;
                    match result {
                        VerifyResult::Equivalent => {
                            // Proven equivalent — proceed to cost check.
                        }
                        VerifyResult::CounterExample(_) => {
                            // NOT equivalent — reject.
                            return false;
                        }
                        VerifyResult::Timeout => {
                            // Could not prove in time — be conservative, reject.
                            return false;
                        }
                        VerifyResult::Inconclusive(_) => {
                            // Without Z3, test-vector filter passed but no
                            // formal proof. In full_mode we require proof; in
                            // fast mode we accept test-vector-only validation.
                            if full_mode {
                                return false;
                            }
                            // In fast mode, accept the candidate on the basis
                            // of the test-vector filter alone.
                        }
                    }
                }
            }

            // When core-superopt is not enabled, we rely on MCTS's own
            // internal test-vector verification (happens inside
            // `MctsSuperoptimizer::optimize`). No additional check needed.

            // ── Stage 5: uarch Cost Validation ───────────────────────────
            #[cfg(feature = "core-superopt")]
            {
                if self.config.use_uarch_cost_model {
                    // S13 fix: Cache TargetConfig instead of calling detect()
                    // (which executes CPUID) per expression.
                    let target = *self.cached_target.get_or_insert_with(TargetConfig::detect);
                    let original_cost = estimate_instr_cycles(&original_instr, target);
                    let candidate_cost = estimate_instr_cycles(&candidate_instr, target);

                    // Only accept if the candidate is strictly cheaper.
                    // (Equal cost rewrites are rejected to avoid churn.)
                    if candidate_cost >= original_cost {
                        self.stats.uarch_rejections += 1;
                        return false;
                    }
                }
            }

            // ── Stage 6: Machine-Instruction-Aware Optimization ────────
            //
            // When the machine-instr-model feature is enabled, lower the
            // candidate to machine instructions and run the instruction-aware
            // pipeline: legality check → multi-objective cost model →
            // bounded validation. This catches cases where the expression-level
            // MCTS found something that looks good algebraically but is worse
            // at the machine level (e.g., more spills, worse port pressure).
            #[cfg(feature = "machine-instr-model")]
            {
                self.stats.machine_instr_candidates += 1;

                // Lower both original and candidate to machine instruction blocks
                let orig_block = lower_instr_to_machine(&original_instr);
                let cand_block = lower_instr_to_machine(&candidate_instr);

                // Run the machine-instr pipeline on the candidate
                let target = *self.cached_target.get_or_insert_with(TargetConfig::detect);
                let pipeline = MachineInstrPipeline::new(target);

                // Score both blocks with the multi-objective cost model
                let orig_cost = estimate_machine_cost(&orig_block, target);
                let cand_cost = estimate_machine_cost(&cand_block, target);

                // Reject if the machine-level cost is not better
                if cand_cost.weighted_cost() >= orig_cost.weighted_cost() {
                    self.stats.machine_instr_rejections += 1;
                    return false;
                }

                // Validate the transformation at the machine level
                let valid = validate_transformation(
                    &orig_block, &cand_block, pipeline.validation_tests,
                );
                match valid {
                    crate::optimizer::machine_instr_model::ValidationResult::ProvenEquivalent
                    | crate::optimizer::machine_instr_model::ValidationResult::BoundedTestPassed { .. } => {
                        // Machine-level validation passed
                    }
                    _ => {
                        // Machine-level validation failed — reject
                        self.stats.machine_instr_rejections += 1;
                        return false;
                    }
                }
            }

            // ── All checks passed — apply the optimization ───────────────
            self.stats.mcts_candidates_verified += 1;
            self.stats.expressions_optimized += 1;
            *expr = candidate_expr;
            return true;
        }

        #[cfg(not(any(feature = "core-superopt", feature = "gnn-optimizer")))]
        {
            let _ = (expr, is_hot, full_mode);
            false
        }
    }
}

