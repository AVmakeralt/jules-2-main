// =========================================================================
// Unified Superoptimizer Pass — Wired Pipeline
//
// This module connects the superoptimizer ecosystem to the compilation
// pipeline. It runs a multi-stage optimization pipeline:
//
//   Stage 1: Semantic Rewrites (120+ egg-style rules)
//   Stage 2: Known-Bits Pruning (fast pre-filter)
//   Stage 3: MCTS Tree Search (stochastic superoptimization)
//   Stage 4: SMT Verification (CEGIS + optional Z3)
//   Stage 5: uarch Cost Validation (dependency-DAG scheduling)
//
// Feature gates:
//   - core-superopt: Stages 2-5
//   - gnn-optimizer: Stage 1 (in addition to 2-5 when both enabled)
//   - smt-verify: Z3 formal proofs in Stage 4
//
// SuperoptMode from the AST drives the optimization level:
//   - None          → skip superoptimization entirely
//   - MctsSuperopt  → MCTS pipeline (stages 2-5), no semantic rewrites
//   - EGraph        → semantic rewrites + MCTS fast config
//   - MlSuperopt    → semantic rewrites + MCTS fast config
//   - All           → full pipeline including SMT verification
// =========================================================================

